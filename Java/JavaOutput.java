
/*
-: Output in Java
==========
    - There are two ways to get output in java
        - println()
        - print()
*/

// Example
public class JavaOutput{
    public static void main(String args[]) {
        // Example of println();

        // Print the String
        System.out.println("Hello World !!");
        System.out.println("Welcome to the world of Code Scroller !!");

        // Print Integer Value
        System.out.println(100);

        // Print Expression
        System.out.println(3+4+5);

        // Example of print();

        System.out.print("Hello World! ");
        System.out.print("I am also in the same line.");

    }
}

/* 
- The print() method, which is similar to println().
- Note : The only difference is that it does not insert a new line at the end of the output:
 */